package main

import "fmt"

func main() {
	var texto string
	fmt.Println(texto)

	var numero int
	fmt.Println(numero)

	var booleano bool
	fmt.Println(booleano)

	var ponteiro *int
	fmt.Println(ponteiro)

	var slice []int
	fmt.Println(slice)

	var m map[string]int
	fmt.Println(m)

	var funcao func() string
	fmt.Println(funcao)

	var inter interface{}
	fmt.Println(inter)

	var canal chan int
	fmt.Println(canal)
}
